# -- 29: Dosage het rate profile ──
build_dosage_het_profile <- function(chr) {
  il_chr <- if (nrow(inv_like_dt) > 0) inv_like_dt[chrom == chr] else data.table()
  if (nrow(il_chr) == 0) return(NULL)
  has_mean <- "dosage_het_rate_median" %in% names(il_chr) && any(is.finite(il_chr$dosage_het_rate_median))
  has_cv <- "dosage_het_rate_cv" %in% names(il_chr) && any(is.finite(il_chr$dosage_het_rate_cv))
  if (!has_mean && !has_cv) return(NULL)
  il_chr[, pos_mb := (start_bp + end_bp) / 2e6]

  tracks <- list()
  if (has_mean) {
    tracks[["median"]] <- data.table(pos_mb = il_chr$pos_mb,
      value = il_chr$dosage_het_rate_median,
      track = "A: Median het rate (across 226 samples)")
  }
  if (has_cv) {
    tracks[["cv"]] <- data.table(pos_mb = il_chr$pos_mb,
      value = il_chr$dosage_het_rate_cv,
      track = "B: Het rate CV (bimodality signal)")
  }
  if ("dosage_het_rate_sd" %in% names(il_chr) && any(is.finite(il_chr$dosage_het_rate_sd))) {
    tracks[["sd"]] <- data.table(pos_mb = il_chr$pos_mb,
      value = il_chr$dosage_het_rate_sd,
      track = "C: Het rate SD")
  }

  pdt <- rbindlist(tracks)
  pdt <- pdt[is.finite(value)]
  pdt[, track := factor(track, levels = unique(track))]

  p <- ggplot(pdt, aes(x = pos_mb, y = value)) +
    geom_point(size = 0.3, alpha = 0.4, color = "darkgreen") +
    facet_wrap(~ track, ncol = 1, scales = "free_y") +
    labs(x = paste0(chr, " (Mb)"), y = NULL,
         title = paste0(chr, " -- Dosage Het Rate Profile (Independent from PCA)"),
         subtitle = "From raw dosage: fraction of sites with dosage in [0.3, 1.7] per sample",
         caption = paste0("Source: raw allele dosage matrix, NOT PC1 projection\n",
                         "A: MEDIAN het level (robust to outliers, higher in diverse regions)\n",
                         "B: CV = sd/median — HIGH CV = some samples very het, others hom = INVERSION SIGNAL\n",
                         "C: raw SD | Requires --dosage_dir in precomp")) +
    THEME_BASE +
    theme(strip.text = element_text(face = "bold", size = 8))
  p
}

