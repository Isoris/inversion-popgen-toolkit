}

# -- 32: PC1 het metrics profile (gap + mid_fraction + mid_variance) ──
build_het_metrics_profile <- function(chr) {
  il_chr <- if (nrow(inv_like_dt) > 0) inv_like_dt[chrom == chr] else data.table()
  if (nrow(il_chr) == 0) return(NULL)
  il_chr[, pos_mb := (start_bp + end_bp) / 2e6]

  tracks <- list()
  if ("het_pc1_gap" %in% names(il_chr) && any(is.finite(il_chr$het_pc1_gap)))
    tracks[["gap"]] <- data.table(pos_mb = il_chr$pos_mb,
      value = il_chr$het_pc1_gap, track = "A: PC1 gap (min dist between band centers)")
  if ("het_mid_fraction" %in% names(il_chr) && any(is.finite(il_chr$het_mid_fraction)))
    tracks[["mid"]] <- data.table(pos_mb = il_chr$pos_mb,
      value = il_chr$het_mid_fraction, track = "B: Middle band fraction (expected ~0.5 for inv)")
  if ("pc1_bimodality" %in% names(il_chr) && any(is.finite(il_chr$pc1_bimodality)))
    tracks[["bimod"]] <- data.table(pos_mb = il_chr$pos_mb,
      value = il_chr$pc1_bimodality, track = "C: PC1 bimodality (Ashman D)")
  if ("local_delta12" %in% names(il_chr) && any(is.finite(il_chr$local_delta12)))
    tracks[["d12"]] <- data.table(pos_mb = il_chr$pos_mb,
      value = il_chr$local_delta12, track = "D: \u039412 from fixed-band soft membership")

  if (length(tracks) < 2) return(NULL)
  pdt <- rbindlist(tracks)
  pdt <- pdt[is.finite(value)]
  pdt[, track := factor(track, levels = unique(track))]

  p <- ggplot(pdt, aes(x = pos_mb, y = value)) +
    geom_point(size = 0.3, alpha = 0.4, color = "steelblue") +
    facet_wrap(~ track, ncol = 1, scales = "free_y") +
    labs(x = paste0(chr, " (Mb)"), y = NULL,
         title = paste0(chr, " -- Het Metrics Profile (PC1-derived)"),
         subtitle = "Multiple het assessment channels from k=3 band analysis",
         caption = paste0("A: Min gap between k=3 centers — large gap = discrete groups\n",
                         "B: Fraction in middle band — ~0.50 for balanced common inversion\n",
                         "C: Ashman D between outer bands — high = bimodal = het signal\n",
                         "D: \u039412 from chr-wide fixed-band soft memberships\n",
                         "NOTE: all PC1-derived, not independent from eigenvalues")) +
    THEME_BASE +
    theme(strip.text = element_text(face = "bold", size = 7))
  p
}

