# -- 27: Three-track decomposition profile (structure + inv + family) ──
build_three_track_profile <- function(chr) {
  il_chr <- if (nrow(inv_like_dt) > 0) inv_like_dt[chrom == chr] else data.table()
  if (nrow(il_chr) == 0) return(NULL)
  has_fam <- "family_likeness" %in% names(il_chr) && any(is.finite(il_chr$family_likeness))
  has_str <- "structure_likeness" %in% names(il_chr) && any(is.finite(il_chr$structure_likeness))
  if (!has_str) return(NULL)
  il_chr[, pos_mb := (start_bp + end_bp) / 2e6]

  tracks <- list()
  tracks[["Structure"]] <- data.table(pos_mb = il_chr$pos_mb, value = il_chr$structure_likeness,
                                       track = "A: Structure-likeness (PVE1)")
  tracks[["Inversion"]] <- data.table(pos_mb = il_chr$pos_mb, value = il_chr$inv_likeness,
                                       track = "B: Inv-likeness (50% het + 25% PVE + 25% dip)")
  if (has_fam)
    tracks[["Family"]] <- data.table(pos_mb = il_chr$pos_mb, value = il_chr$family_likeness,
                                      track = "C: Family-likeness (Q variance by bands)")

  pdt <- rbindlist(tracks)
  pdt <- pdt[is.finite(value)]
  pdt[, track := factor(track, levels = unique(track))]

  p <- ggplot(pdt, aes(x = pos_mb, y = value)) +
    geom_point(size = 0.3, alpha = 0.4, color = "steelblue") +
    geom_hline(yintercept = 0.5, linetype = "dashed", color = "grey50", linewidth = 0.3) +
    facet_wrap(~ track, ncol = 1, scales = "free_y") +
    labs(x = paste0(chr, " (Mb)"), y = "Score [0-1]",
         title = paste0(chr, " -- Three-Track Decomposition"),
         subtitle = paste0("A: captures all structure | B: inversion-specific | C: family-driven"),
         caption = paste0("Ideal inversion: A high + B high + C low\n",
                         "Family block: A high + B moderate + C high\n",
                         "Background: all low | Dashed = 0.5 threshold")) +
    THEME_BASE +
    theme(strip.text = element_text(face = "bold", size = 8),
          panel.spacing = unit(0.5, "lines"))
  p
}

